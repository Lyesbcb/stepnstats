import {
  StyleSheet,
  Text,
  View,
  Alert,
} from "react-native";
import React, { useEffect, useState } from "react";
import Header from "./header";
import { getAllTemporality } from "../../services/mps/index";
import Marketplace from "./marketplace";
import ProgressLoader from "rn-progress-loader";
import { RFValue } from "react-native-responsive-fontsize";

export default function MarketplaceScreen({ navigation }) {
  const [selectedContent, setSelectedContent] = useState([]);
  const [selectedRealm, setSelectedRealm] = useState("Solana");
  const [mpsDay, setMpsDay] = useState([]);
  const [mpsWeek, setMpsWeek] = useState([]);
  const [mpsMonth, setMpsMonth] = useState([]);
  const [selectedTab, SetSelectedTab] = useState(0);
  const [loading, setLoading] = useState(false);
  useEffect(() => {
    myFunction();
  }, []);

  const myFunction = async (realm) => {
    try {
      if (realm) {
        setLoading(true);
        await setMpsDay(await getAllTemporality(realm, "Day"));
        await setMpsWeek(await getAllTemporality(realm, "Week"));
        await setMpsMonth(await getAllTemporality(realm, "Month"));
        setLoading(false);
      } else {
        setLoading(true);
        await setMpsDay(await getAllTemporality(selectedRealm, "Day"));
        await setMpsWeek(await getAllTemporality(selectedRealm, "Week"));
        await setMpsMonth(await getAllTemporality(selectedRealm, "Month"));
        setLoading(false);
      }
    } catch (err) {
      console.log(err);
      Alert.alert("Error");
    }
  };
  return (
    <View style={{ width: "100%", height: "100%" }}>
      <ProgressLoader
        visible={loading}
        isModal={true}
        isHUD={true}
        hudColor={"#000000"}
        color={"#FFFFFF"}
      />
      <View style={styles.container2}>
        <Header
          navigation={navigation}
          styles={styles}
          selectedTab={selectedTab}
          SetSelectedTab={SetSelectedTab}
        />
        <View
          style={{
            position: "absolute",
            top: "15%",
            height: "100%",
            width: "100%",
            alignItems: "center",
            alignContent: "center",
          }}
        >
          {selectedTab === 0 ? (
            <Marketplace
              selectedContent={selectedContent}
              setSelectedContent={setSelectedContent}
              selectedRealm={selectedRealm}
              navigation={navigation}
              mpsDay={mpsDay}
              mpsWeek={mpsWeek}
              mpsMonth={mpsMonth}
              myFunction={myFunction}
              setSelectedRealm={setSelectedRealm}
            ></Marketplace>
          ) : selectedTab === 1 ? (
            <View>
              <Text>{selectedTab}</Text>
            </View>
          ) : (
            <View></View>
          )}
        </View>

        {/* <Footer styles={styles}></Footer> */}
      </View>
    </View>
  );
}

